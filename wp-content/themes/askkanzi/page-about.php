    <div class="time-container time-left">
      <div class="time-content">
        <h2>1968</h2>
        <p>Dr. Duane Rumbaugh conducts comparative studies on the ability of nonhuman primates to reason, plan, and engage in inferential thinking. He finds that only apes are to able engage in human-like reasoning processes on the transfer-index task at the San Diego Zoo. He is a member of the faculty at San Diego College. He obtained his Ph. D from the University of Colorado in 1955.
      </p>
      <p>
        The bonobos Matata and Bosondjo are born in the Congo- they are probably related to the older femal Lokelama, and all three will be caught together in one net in 1975 and brought to the Yerkes Regional Primate Research Center. Their offspring now reside in Iowa.
      </p>
      <p>
         Three wild-caught bonobos arrive at the Yerkes Primate Center, on lend-lease from the Congolese government, under the auspices of a program funded by the office of Navel Research, to determine if they are new species of ape, or a subspecies of the chimpanzee. The stated goal is to return these three bonobos to the Congo within a few years and to establish a federally funded research center in the Congo, after differences in their reproductive cycle and that of the chimpanzee are established. This project was initiated by Carl D’jerrassi the inventor of the birth control pill.
      </p>
      </div>
    </div>
    <div class="time-container time-right">
      <div class="time-content">
        <h2>1970</h2>
        <p>The Language Analog Research Project begins at the Yerkes Primate Center. It is funded by NICHD and directed by Dr. Duane Rumbaugh who is the Associate Director of the Yerkes Primate Research Center. 
  2. There are initially two ape subjects, a baby orangutan (BJ) and a baby chimpanzee (Lana), both females.  The original room for the project is 8 x 10 feet. </p>
  <p>
     Dr. Savage-Rumbaugh begins her graduate program at the University of Oklahoma working chimpanzees Lucy, Booee, Bruno, Alle and Wahsoe who are acquiring sign language under different rearing conditions as well as  with Petal, Pampy, Carrie, Shadrack, Meshak, Mimi, and Manny – chimpanzees being reared by their natural mothers in a mother-infant group. Her focus is upon language and nonverbal behavior in apes, as a function of differential rearing environments. 
  </p>
      </div>
    </div>
    <div class="time-container time-left">
      <div class="time-content">
        <h2>1973</h2>
        <p>Rumbaugh, D. M.  (1973).  The importance of nonhuman primate studies of learning and related phenomena for understanding cognitive development.  In G. H. Bourne (Ed.), Nonhuman Primates and Medical Research (pp 415-429).  New York:  Academic Press. (With this article Dr. Rumbaugh became  the first scientist to develop a theoretical framework for  investigating the evolution of cognitive and learning skills of nonhuman primates in a way that allows the inclusion of human cognition.)</p>
        <p> Rumbaugh, D. M., Gill, T. V., & von Glasersfeld, E. C.  (1973).  Reading and sentence completion by a chimpanzee (Pan).  Science, 182, 731-733.(This report of Lana’s ability to read and complete sentences- by the age of three - was truly ground-breaking. While “language” was not claimed, the ability to literally “read” and “complete” acquired sequences was considered to be a stunning revelation.)
  </p>
      </div>
    </div>
    <div class="time-container time-right">
      <div class="time-content">
        <h2>1975</h2>
        <p>Dr. Savage-Rumbaugh completes her dissertation “Mother-Infant Behavior Among Captive Group-Living Chimpanzees”  Un. of Oklahoma. This study documented many deliberate intentional nonverbal signals, devised between mother and infant chimpanzees. </p>
        <p> Dr. Savage-Rumbaugh joins the LANA project, begins behavioral research with bonobos. </p>
      </div>
    </div>
    <div class="time-container time-left">
      <div class="time-content">
        <h2>1976</h2>
        <p>Rumbaugh, D. M., & Gill, T. V.  (1976).  The mastery of language- type skills by the chimpanzee (Pan).  Annals of the New York Academy of Sciences, 1976, 280, 562-578. This is the first article to clearly demonstrate the existence of the basic components of language; symbols, syntax, dialogue and novel appropriate usage, in a nonhuman. It had an immense impact on the field, and led to the rearing of chimpanzee Nim, under a strict behaviorist program.</p>
      </div>
    </div>
  </div>

