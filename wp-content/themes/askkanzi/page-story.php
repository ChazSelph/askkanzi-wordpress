        <p>Rumbaugh, D. M., & Gill, T. V.  (1976).  The mastery of language- type skills by the chimpanzee (Pan).  Annals of the New York Academy of Sciences, 1976, 280, 562-578. This is the first article to clearly demonstrate the existence of the basic components of language; symbols, syntax, dialogue and novel appropriate usage, in a nonhuman. It had an immense impact on the field, and led to the rearing of chimpanzee Nim, under a strict behaviorist program.</p>
